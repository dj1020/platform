<div>
	<h1>Honor Code.</h1>
	<p>In order to ensure fairness, all students participating in Coursera courses must agree to abide by the following code of conduct.</p>
</div>

<div>
<ol>
<li style="font-size:28px;font-weight:normal;line-height:52px;">I will register for only one account.</li>
<li style="font-size:28px;font-weight:normal;line-height:52px;">My answers to homework, quizzes and exams will be my own work (except for assignments that explicitly permit collaboration).</li>
<li style="font-size:28px;font-weight:normal;line-height:52px;">I will not make solutions to homework, quizzes or exams available to anyone else. This includes both solutions written by me, as well as any official solutions provided by the course staff.</li>
<li style="font-size:28px;font-weight:normal;line-height:52px;">I will not engage in any other activities that will dishonestly improve my results or dishonestly improve/hurt the results of others.</li>
</ol>
</div>
